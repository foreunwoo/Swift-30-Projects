import UIKit

extension UIColor {
    @nonobjc class var birdNavy: UIColor {
        return UIColor(red: 20 / 255, green: 29 / 255, blue: 38 / 255, alpha: 1)
    }
    
    @nonobjc class var birdBlue: UIColor {
        return UIColor(red: 0 / 255, green: 172 / 255, blue: 237 / 255, alpha: 1)
    }
}
